package com.huilaila.po;

public class JobFavorite {
	Long jobFavoriteId;
	Long userId;
	Long jobId;

	public Long getJobFavoriteId() {
		return jobFavoriteId;
	}

	public void setJobFavoriteId(Long jobFavoriteId) {
		this.jobFavoriteId = jobFavoriteId;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public Long getJobId() {
		return jobId;
	}

	public void setJobId(Long jobId) {
		this.jobId = jobId;
	}

}
